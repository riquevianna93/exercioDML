/*RELATÓRIO GERAL DE TODOS OS CLIENTES(TELEFONE E ENDERECO)*/

SELECT C.IDCLIENTE,C.NOME,C.SEXO,C.EMAIL,C.CPF,E.RUA,E.BAIRRO,E.CIDADE,E.ESTADO,
		T.TIPO,T.NUMERO
FROM CLIENTE C
INNER JOIN ENDERECO E
ON IDCLIENTE = E.ID_CLIENTE
INNER JOIN TELEFONE T
ON IDCLIENTE = T.ID_CLIENTE;

