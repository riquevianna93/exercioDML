/* RELATÓRIO DE HOMENS */

SELECT C.IDCLIENTE,C.NOME,C.SEXO,C.EMAIL,C.CPF,E.RUA,E.BAIRRO,E.CIDADE,E.ESTADO,
		T.TIPO,T.NUMERO
FROM CLIENTE C
INNER JOIN ENDERECO E
ON IDCLIENTE = E.ID_CLIENTE
INNER JOIN TELEFONE T
ON IDCLIENTE = T.ID_CLIENTE
WHERE SEXO = 'M';


/* RELATÓRIO DE MULHERES */

SELECT C.IDCLIENTE,C.NOME,C.SEXO,C.EMAIL,C.CPF,E.RUA,E.BAIRRO,E.CIDADE,E.ESTADO,
		T.TIPO,T.NUMERO
FROM CLIENTE C
INNER JOIN ENDERECO E
ON IDCLIENTE = E.ID_CLIENTE
INNER JOIN TELEFONE T
ON IDCLIENTE = T.ID_CLIENTE
WHERE SEXO = 'F';


/* QUANTIDADE DE HOMENS E MULHERES */

SELECT COUNT(*) AS QUANTIDADE,SEXO 
FROM CLIENTE
GROUP BY SEXO;

/* IDS E EMAIL DAS MULHERES QUE MORAM NO CENTRO DO RJ E NAO TENHAM CELULAR */


SELECT 	C.IDCLIENTE,
		IFNULL(C.EMAIL,'******************')AS EMAIL,
		C.NOME,
		C.SEXO,
		T.TIPO,
		E.BAIRRO,
